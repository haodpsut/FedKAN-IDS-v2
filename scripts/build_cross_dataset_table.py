"""Auto-generate the cross-dataset replication table from results/runs/.

Scans all e1_<dataset>_* run directories, picks the heterogeneity-robustness
cell (binary, Dirichlet alpha=0.1), and emits results/tables/cross_dataset_placeholder.tex
with the worst-seed accuracy per (variant, dataset) and the KAN-8 vs MLP-PM-80
gap on the bottom row.

Datasets discovered automatically — runs whose dir starts with `e1_<X>_*`
contribute to column X. Missing datasets render as `---`.
"""
from __future__ import annotations
import json
import statistics as stats
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RUNS = ROOT / "results" / "runs"
OUT = ROOT / "results" / "tables" / "cross_dataset_placeholder.tex"

VARIANT_ORDER = ["mlp_h32", "mlp_h80", "kan_h8", "kan_h16"]
VARIANT_LABEL = {
    "mlp_h32":  r"FedAvg-MLP (32h)",
    "mlp_h80":  r"FedAvg-MLP-PM (80h)",
    "kan_h8":   r"\textbf{FedKAN (Ours, 8h)}",
    "kan_h16":  r"F-KAN (16h)",
}
VARIANT_PARAMS = {
    "mlp_h32":  "1{,}346",
    "mlp_h80":  "3{,}362",
    "kan_h8":   "3{,}280",
    "kan_h16":  "6{,}560",
}
DATASET_LABEL = {
    "botiot":  "BoT-IoT-v2",
    "toniot":  "ToN-IoT-v2",
    "cseciic": "CSE-CIC",
}
DATASET_ORDER = ["botiot", "toniot", "cseciic"]


def variant_of(metrics: dict) -> str | None:
    name = metrics.get("model_name")
    h = metrics.get("model_hidden")
    if not name or not h:
        return None
    base = f"{name}_h{'x'.join(str(x) for x in h)}"
    # KAN runs with non-default grid_size are tagged distinctly so a
    # G=10 ablation does not pollute the main G=5 cell.
    g = metrics.get("model_grid_size")
    if name == "kan" and g is not None and g != 5:
        base += f"_g{g}"
    return base


def dataset_of(run_dir_name: str) -> str | None:
    """Strip 'e1_' prefix; the next underscore-separated token is the dataset."""
    if not run_dir_name.startswith("e1_"):
        return None
    rest = run_dir_name[3:]
    return rest.split("_", 1)[0]


def collect() -> dict:
    """{(dataset, variant): [accuracy across seeds]} for binary Dir(alpha=0.1)."""
    bucket: dict = {}
    for d in sorted(RUNS.iterdir()):
        if not d.is_dir() or d.name.startswith("smoke") or d.name.startswith("e1_mini"):
            continue
        mj = d / "metrics.json"
        if not mj.exists():
            continue
        m = json.loads(mj.read_text())
        if (m.get("data_partition") != "dirichlet"
                or m.get("data_alpha") != 0.1
                or m.get("data_mode") != "binary"):
            continue
        v = variant_of(m)
        ds = dataset_of(d.name)
        if v is None or ds is None:
            continue
        bucket.setdefault((ds, v), []).append(m["final_metrics"]["accuracy"])
    return bucket


def main():
    bucket = collect()
    datasets_present = sorted({k[0] for k in bucket}, key=lambda d: DATASET_ORDER.index(d) if d in DATASET_ORDER else 99)
    if not datasets_present:
        print("no e1_* binary Dir(0.1) runs found")
        return

    # Collect n per (dataset) for caption.
    n_per_ds = {}
    for ds in datasets_present:
        ns = [len(bucket[(ds, v)]) for v in VARIANT_ORDER if (ds, v) in bucket]
        n_per_ds[ds] = max(ns) if ns else 0

    # Caption sentence
    cap_parts = []
    for ds in DATASET_ORDER:
        label = DATASET_LABEL[ds]
        if ds in datasets_present:
            cap_parts.append(f"{label} ($n{{=}}{n_per_ds[ds]}$)")
        else:
            cap_parts.append(f"{label} (pending)")
    cap = (
        r"Cross-dataset replication: \emph{worst-seed} accuracy (\%) on the "
        r"heterogeneity-robustness cell (binary classification, Dirichlet "
        r"$\alpha = 0.1$). Datasets: "
        + "; ".join(cap_parts)
        + r". Bold = best per column."
    )

    # Build LaTeX
    lines = [
        r"% Auto-generated by scripts/build_cross_dataset_table.py",
        r"\begin{table}[t]",
        r"\centering",
        f"\\caption{{{cap}}}",
        r"\label{tab:cross_dataset}",
        r"\renewcommand{\arraystretch}{1.15}",
        r"\setlength{\tabcolsep}{4pt}",
        r"\begin{tabular}{@{}l c " + " ".join(["c"] * len(DATASET_ORDER)) + r"@{}}",
        r"\toprule",
    ]
    head = ["Method", "Params"] + [DATASET_LABEL[d] for d in DATASET_ORDER]
    lines.append(" & ".join(head) + r" \\")
    sub = ["", ""] + [
        f"($n{{=}}{n_per_ds[d]}$)" if d in datasets_present else "(pending)"
        for d in DATASET_ORDER
    ]
    lines.append(" & ".join(sub) + r" \\")
    lines.append(r"\midrule")

    # Find best per-dataset column for bolding
    best_per_ds = {}
    for ds in datasets_present:
        cells = [(v, min(bucket[(ds, v)])) for v in VARIANT_ORDER if (ds, v) in bucket]
        if cells:
            best_per_ds[ds] = max(cells, key=lambda x: x[1])[0]

    for v in VARIANT_ORDER:
        row = [VARIANT_LABEL[v], VARIANT_PARAMS[v]]
        for ds in DATASET_ORDER:
            if (ds, v) in bucket:
                worst = min(bucket[(ds, v)])
                cell = f"{worst*100:.2f}"
                if best_per_ds.get(ds) == v:
                    cell = r"\textbf{" + cell + "}"
                row.append(cell)
            else:
                row.append(r"---")
        lines.append(" & ".join(row) + r" \\")

    # Bottom row: KAN-8 vs MLP-PM-80 gap per column
    lines.append(r"\midrule")
    gap_row = [r"\multicolumn{2}{@{}l}{KAN-8 advantage over MLP-PM-80}"]
    for ds in DATASET_ORDER:
        if (ds, "kan_h8") in bucket and (ds, "mlp_h80") in bucket:
            gap = (min(bucket[(ds, "kan_h8")]) - min(bucket[(ds, "mlp_h80")])) * 100
            gap_row.append(f"${gap:+.2f}$\\,pp")
        else:
            gap_row.append(r"---")
    lines.append(" & ".join(gap_row) + r" \\")

    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]

    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {OUT}")
    print(f"Datasets present: {datasets_present}")
    for ds in datasets_present:
        print(f"  {ds} (n_max={n_per_ds[ds]}):")
        for v in VARIANT_ORDER:
            if (ds, v) in bucket:
                accs = bucket[(ds, v)]
                print(f"    {v:10s}  worst={min(accs)*100:6.2f}  "
                      f"mean={stats.mean(accs)*100:6.2f}  n={len(accs)}")


if __name__ == "__main__":
    main()
